
// The == (equal) Operator :compare value only
let x7 = 5;

console.log(x7 == 8); // false
console.log(10 == "10") // true ; even "10" is string 

// The === (strict equal) Operator : Compare vlue and type 
console.log(6 === 6); // true
console.log(6 === "6"); // false

// The != (not equal) Operator
console.log(11 != 8) ;     // compare value only
console.log(11 != "11");  // compare value only

// The !== (strict unequal) Operator
console.log(5 !== 5);  // not equal value or not equal type
console.log(5 !== "5"); // not equal value or not equal typec
