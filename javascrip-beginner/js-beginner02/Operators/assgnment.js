
// JavaScript Assignment Operators

// Addition Assignment
let x = 10 ;;
x += 5;

console.log(x)

// Subtraction Assignment  
let y = 20 ;
y -= 10 ;

console.log(y)

// Exponentiation Assignment
let a1 = 10;
a1 **= 5;
console.log(a1)

// The Division Assignment Operator 
let b1 = 10;
b1 /= 5;
console.log(b1)

// String Assignment
// Addition Assignment
let text = 'Hello';
text += " World";
console.log(text)

// The Logical AND assignment operator &&
let k1 = true;
let f1 = k1 &&= 10;
console.log(f1)